package baseical;

public class TypeTsst01 {
	
	public static void main(String[] args){
		
		//double转String
		double x=88;
		double y=89;
		
		 String a2=Double.toString(x);
		 String a3=Double.toString(y);
		 
		 System.out.println("a2:"+a2+"  "+"a3:"+a3);
		 System.out.println("a2+a3="+a2+a3);
		 
		 
		 
		// float转String
		 float x1=88;
		 float y1=89;
			
			 String a4=Float.toString(x1);
			 String a5=Float.toString(y1);
			 
			 System.out.println("a4:"+a4+"  "+"a5:"+a5);
			 System.out.println("a2+a3="+a4+a5);
	}

}
